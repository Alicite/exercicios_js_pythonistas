// Crie um array chamado cores com pelo menos 3 cores.
const cores = ['azul', 'vermelho', 'verde'];
console.log(cores);

// Adicione uma nova cor ao final do array.
cores.push("branco");
cores.unshift("preto");
console.log(cores);

// Remova a primeira cor do array.
cores.shift()
cores.pop()
console.log(cores);

