let num1 = 35
let num2 = 20

console.log(num1, "-", num2, " = ", num1-num2)
console.log(num1, "+", num2, " = ", num1+num2)
console.log(num1, "*", num2, " = ", num1*num2)
console.log(num1, "/", num2, " = ", num1/num2)

if (num1>num1) {
    console.log(num1, "é maior que ", num2)
}
else {
    console.log(num1, "é menor que ", num2)
}

if (num1===num1) {
    console.log(num1, "é igual a ", num2)
}
else {
    console.log(num1, "não é igual a ", num2)
}